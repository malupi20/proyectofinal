document.getElementById('verMasBtn1').addEventListener('click', function() {
    var info = document.getElementById('info1');
    if (info.classList.contains('info-hidden')) {
        info.classList.remove('info-hidden');
        info.classList.add('info-visible');
        this.textContent = 'Ocultar información';
    } else {
        info.classList.remove('info-visible');
        info.classList.add('info-hidden');
        this.textContent = 'Ver más información';
    }
});

